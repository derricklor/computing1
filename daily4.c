#include <stdio.h>

int main(int argc, char* argv[])
{
	printf("%d is an integer and can be printed using %%d.\n", 42);
	printf("\nThe character '%c' can be printed using %%c.\n", 'J');
	printf("\nFloating point numbers use %%f. %f is an example.\n", 3.14159);
	return 0;
}